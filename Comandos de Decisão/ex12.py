# Faça um programa que leia um valor digitado pelo usuário e imprima os resultados com base na tabela.
# 1: Bom Dia - Boa Tarde - Boa Noite
# 2: Por Favor :) - Com Licenca :D - Muito Obrigado ;)
# 3: Por Gentileza - Voce poderia - Desculpe
# 4: Boa Sorte - Tenha Fe
# Outro: Estudar vale muito a pena!

escolha = int(input())

if escolha == 1: print("Bom Dia - Boa Tarde - Boa Noite")
elif escolha == 2: print("Por Favor :) - Com Licenca :D - Muito Obrigado ;)")
elif escolha == 3: print("Por Gentileza - Voce poderia - Desculpe")
elif escolha == 4: print("Boa Sorte - Tenha Fe")
else: print("Estudar vale muito a pena!")